package com.bean;

public class MusicInfo {
	private int id;
	private String music_name;
	private String music_song;
	private String music_type;
	private String music_route;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMusic_name() {
		return music_name;
	}
	public void setMusic_name(String music_name) {
		this.music_name = music_name;
	}
	public String getMusic_song() {
		return music_song;
	}
	public void setMusic_song(String music_song) {
		this.music_song = music_song;
	}
	public String getMusic_type() {
		return music_type;
	}
	public void setMusic_type(String music_type) {
		this.music_type = music_type;
	}
	public String getMusic_route() {
		return music_route;
	}
	public void setMusic_route(String music_route) {
		this.music_route = music_route;
	}
	
}
